<h3>Boris Schlossberg</h3>
<div class="role">Co-Founder · Futures &amp; Prop Trading</div>
<p>Boris began his Wall Street career over two decades ago at Drexel Burnham Lambert, trading everything from equities and options to index futures and FX. He served as Senior Currency Strategist at FXCM and Director of Currency Research at GFT before co-founding BK with Kathy Lien. A weekly contributor to CNBC's Squawk Box and author of <em>Technical Analysis of the Currency Market</em> and <em>Millionaire Traders</em>, Boris built the momentum-based techniques behind BK's strategies, and trades them live every day.</p>
