<h3>Kathy Lien</h3>
<div class="role">Co-Founder · The Queen of Macro Forex Trader</div>
<p>The Queen of Macro graduated from NYU Stern at just 18 and became the youngest trader ever at JPMorgan, starting on the interbank FX desk before moving to cross-markets proprietary trading. She launched DailyFX.com as Chief Strategist in 2003 and co-founded BK with Boris. A CNBC regular quoted by the Wall Street Journal, Reuters and Bloomberg, Kathy is the author of <em>Day Trading and Swing Trading the Currency Market</em> and her newest book, <em>Prop Trading Secrets</em>. Her blend of macro insight and chart precision is the backbone of BK's strategies.</p>
